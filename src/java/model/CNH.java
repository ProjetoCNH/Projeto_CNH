package model;

/**
 *
 * @author alunos
 */
public class CNH {
    
    private int numregistro;
    private String validade;
    private String dtEmissao;
    private String cpf;
    private String categoria;
    private String nomeAutoescola;
    private String orgEmissor;
    private int pontosCarteira;

    /**
     * @return the numregistro
     */
    public int getNumregistro() {
        return numregistro;
    }

    /**
     * @param numregistro the numregistro to set
     */
    public void setNumregistro(int numregistro) {
        this.numregistro = numregistro;
    }

    /**
     * @return the validade
     */
    public String getValidade() {
        return validade;
    }

    /**
     * @param validade the validade to set
     */
    public void setValidade(String validade) {
        this.validade = validade;
    }

    /**
     * @return the dtEmissao
     */
    public String getDtEmissao() {
        return dtEmissao;
    }

    /**
     * @param dtEmissao the dtEmissao to set
     */
    public void setDtEmissao(String dtEmissao) {
        this.dtEmissao = dtEmissao;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the nomeAutoescola
     */
    public String getNomeAutoescola() {
        return nomeAutoescola;
    }

    /**
     * @param nomeAutoescola the nomeAutoescola to set
     */
    public void setNomeAutoescola(String nomeAutoescola) {
        this.nomeAutoescola = nomeAutoescola;
    }

    /**
     * @return the orgEmissor
     */
    public String getOrgEmissor() {
        return orgEmissor;
    }

    /**
     * @param orgEmissor the orgEmissor to set
     */
    public void setOrgEmissor(String orgEmissor) {
        this.orgEmissor = orgEmissor;
    }

    /**
     * @return the pontosCarteira
     */
    public int getPontosCarteira() {
        return pontosCarteira;
    }

    /**
     * @param pontosCarteira the pontosCarteira to set
     */
    public void setPontosCarteira(int pontosCarteira) {
        this.pontosCarteira = pontosCarteira;
    }
            
    
}
